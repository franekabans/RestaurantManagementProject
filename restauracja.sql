CREATE OR REPLACE FUNCTION usun() RETURNS TRIGGER AS $example_table$
   BEGIN
      DELETE from dostawa where id_dostaw = old.id_dostawca;
      RETURN NEW;
   END;
$example_table$ LANGUAGE plpgsql;


